#include "libft.h"

int		ft_putnbr(int n)
{
	char	tab[32];
	int		i;

	i = 0;
	if (n == 0)
		ft_putchar('0');
	while (n)
	{
		tab[i] = n % 10 + '0';
		n = n / 10;
		i++;
	}
	tab[i] = '\0';
	i--;
	while (i >= 0)
	{
		write(1, &tab[i], 1);
		i--;
	}
	return (0);
}
